public class Calculate {

	public int add(int a, int b) {
		return a + b;
	}

	public int sub(int a, int b) {
		return a - b;
	}

	public int ride(int a, int b) {
		return a * b;
	}

	public int division(int a, int b) {
		return a / b;
	}
}
